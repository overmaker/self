package cn.kepu.self.util;

/**
 *
 * @author 劉一童
 * @param <X>
 * @param <Y>
 */
public final class BinaryPair<X, Y> {

    private X v1;
    private Y v2;

    public X getV1() {
        return v1;
    }

    public void setV1(X v1) {
        this.v1 = v1;
    }

    public Y getV2() {
        return v2;
    }

    public void setV2(Y v2) {
        this.v2 = v2;
    }

}
